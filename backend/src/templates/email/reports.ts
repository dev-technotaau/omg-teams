import {
  type EmailTemplate,
  BRAND,
  emailLayout,
  heading,
  paragraph,
  button,
  detailsTable,
  keyValue,
  signature,
  smallText,
} from "./_layout.js";

// ──────────────────────────────────────────────
//  Report Emails
// ──────────────────────────────────────────────

export const dailyReport = (
  reportDate: string,
  reportType: string,
  downloadLink: string,
): EmailTemplate => ({
  subject: `${BRAND.name} — Daily Report for ${reportDate}`,
  html: emailLayout(
    `${heading("Daily Report")}
    ${paragraph("Your daily report has been generated and is ready for download.")}
    ${detailsTable(keyValue("Date", reportDate) + keyValue("Report Type", reportType))}
    ${button("Download Report", downloadLink)}
    ${smallText("This report was automatically generated by the OMG Teams platform.")}
    ${signature()}`,
    `Daily report for ${reportDate} is ready.`,
  ),
  text: `Daily Report for ${reportDate} (${reportType}) is ready. Download: ${downloadLink}`,
});

