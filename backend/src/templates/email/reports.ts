export const scheduledReportDelivery = (
  reportName: string,
  reportType: string,
  generatedAt: string,
  downloadLink: string,
  recipientEmail: string,
): EmailTemplate => ({
  subject: `${BRAND.name} — Scheduled Report: ${reportName}`,
  html: emailLayout(
    `${heading("Scheduled Report Ready")}
    ${paragraph(`Your scheduled report <strong>${reportName}</strong> has been generated successfully.`)}
    ${detailsTable(
      keyValue("Report Name", reportName) +
        keyValue("Report Type", reportType) +
        keyValue("Generated At", generatedAt),
    )}
    ${button("Download Report", downloadLink)}
    ${smallText(`This report was automatically generated and delivered to ${recipientEmail}.`)}
    ${signature()}`,
    `Scheduled report "${reportName}" is ready for download.`,
  ),
  text: `Scheduled Report "${reportName}" (${reportType}) generated at ${generatedAt}. Download: ${downloadLink}`,
});
